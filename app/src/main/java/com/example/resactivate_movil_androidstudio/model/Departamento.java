package com.example.resactivate_movil_androidstudio.model;

public class Departamento {
    int id;
    String nombre;


    //constructor

    public Departamento() {
    }


    public int getId() {
        return id;
    }


    public void setId(int id) {
        this.id = id;
    }

    //get y set
    public String getNombre() {
        return nombre;
    }


    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
